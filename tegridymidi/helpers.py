import os

#=======================================================================================================

def hsv_to_rgb(h, s, v):
    if s == 0.0:
        return v, v, v
    i = int(h*6.0)
    f = (h*6.0) - i
    p = v*(1.0 - s)
    q = v*(1.0 - s*f)
    t = v*(1.0 - s*(1.0-f))
    i = i%6
    return [(v, t, p), (q, v, p), (p, v, t), (p, q, v), (t, p, v), (v, p, q)][i]

#===============================================================================

def generate_colors(n):
    return [hsv_to_rgb(i/n, 1, 1) for i in range(n)]

#===============================================================================

def add_arrays(a, b):
    return [sum(pair) for pair in zip(a, b)]

